#!/usr/bin/env bash
set -euo pipefail

SCRIPT_VERSION="1.0.0"

CLIPROXYAPI_REPO="router-for-me/CLIProxyAPI"
CPAQ_REPO="AllenReder/CLIProxyAPI-Quota-Inspector"

INSTALL_ROOT="${CLIPROXYAPI_INSTALL_ROOT:-$HOME/.local/share/cliproxyapi}"
BIN_DIR="${CLIPROXYAPI_BIN_DIR:-$HOME/.local/bin}"
CONFIG_DIR="${CLIPROXYAPI_CONFIG_DIR:-$HOME/.cli-proxy-api}"
APP_DIR="$INSTALL_ROOT/app"
CONFIG_PATH="${CLIPROXYAPI_APP_CONFIG_PATH:-$APP_DIR/config.yaml}"
SYSTEMD_USER_DIR="${XDG_CONFIG_HOME:-$HOME/.config}/systemd/user"
SERVICE_NAME="cliproxyapi.service"
SERVICE_PATH="$SYSTEMD_USER_DIR/$SERVICE_NAME"

DEFAULT_PORT="${CLIPROXYAPI_PORT:-8318}"
DEFAULT_API_KEY="${CLIPROXYAPI_API_KEY:-sk-my-secret-key}"
DEFAULT_MANAGEMENT_KEY="${CLIPROXYAPI_MANAGEMENT_KEY:-}"
FORCE_CONFIG="${CLIPROXYAPI_FORCE_CONFIG:-0}"

TMP_DIR="$(mktemp -d)"

cleanup() {
  rm -rf "$TMP_DIR"
}
trap cleanup EXIT

log() {
  printf '[install] %s\n' "$*"
}

fail() {
  printf '[install] ERROR: %s\n' "$*" >&2
  exit 1
}

require_cmd() {
  command -v "$1" >/dev/null 2>&1 || fail "Missing required command: $1"
}

detect_arch() {
  case "$(uname -m)" in
    x86_64|amd64)
      printf 'amd64'
      ;;
    aarch64|arm64)
      printf 'arm64'
      ;;
    *)
      fail "Unsupported architecture: $(uname -m)"
      ;;
  esac
}

fetch_release_asset_url() {
  local repo="$1"
  local asset_name="$2"
  local release_json

  release_json="$(curl -fsSL "https://api.github.com/repos/$repo/releases/latest")"

  printf '%s' "$release_json" | python3 -c '
import json
import sys

asset_name = sys.argv[1]
data = json.load(sys.stdin)

for asset in data.get("assets", []):
    if asset.get("name") == asset_name:
        print(asset["browser_download_url"])
        break
else:
    raise SystemExit(f"asset not found: {asset_name}")
' "$asset_name"
}

fetch_release_version() {
  local repo="$1"
  curl -fsSL "https://api.github.com/repos/$repo/releases/latest" | python3 -c '
import json
import sys

data = json.load(sys.stdin)
tag = data["tag_name"]
print(tag[1:] if tag.startswith("v") else tag)
'
}

random_secret() {
  if [ -n "$DEFAULT_MANAGEMENT_KEY" ]; then
    printf '%s' "$DEFAULT_MANAGEMENT_KEY"
    return
  fi

  openssl rand -hex 16
}

ensure_path_line() {
  local bashrc="$HOME/.bashrc"
  local line='export PATH="$HOME/.local/bin:$PATH"'

  touch "$bashrc"
  if ! grep -Fq "$line" "$bashrc"; then
    printf '\n%s\n' "$line" >> "$bashrc"
    log "Added ~/.local/bin to PATH in $bashrc"
  fi
}

