write_cli_wrapper() {
  local target="$1"
  local binary_path="$2"

  cat > "$target" <<EOF
#!/usr/bin/env bash
set -euo pipefail
exec "$binary_path" "\$@"
EOF
  chmod 755 "$target"
}

write_start_wrapper() {
  local target="$1"
  local binary_path="$2"
  local config_path="$3"

  cat > "$target" <<EOF
#!/usr/bin/env bash
set -euo pipefail
exec "$binary_path" -config "$config_path" "\$@"
EOF
  chmod 755 "$target"
}

write_login_wrapper() {
  local target="$1"
  local binary_path="$2"
  local login_flag="$3"
  local config_path="$4"

  cat > "$target" <<EOF
#!/usr/bin/env bash
set -euo pipefail
exec "$binary_path" -config "$config_path" $login_flag "\$@"
EOF
  chmod 755 "$target"
}

write_cpaq_wrapper() {
  local target="$1"
  local binary_path="$2"
  local config_path="$3"
  local key_file="$4"

  cat > "$target" <<EOF
#!/usr/bin/env bash
set -euo pipefail

CONFIG_PATH="\${CPAQ_CONFIG_PATH:-$config_path}"
KEY_FILE="\${CPAQ_MANAGEMENT_KEY_FILE:-$key_file}"
BASE_URL="\${CPAQ_BASE_URL:-}"

if [ ! -x "$binary_path" ]; then
  echo "cpaq binary not found at $binary_path" >&2
  exit 1
fi

if [ ! -f "\$KEY_FILE" ]; then
  echo "management key file not found at \$KEY_FILE" >&2
  exit 1
fi

if [ -z "\$BASE_URL" ]; then
  PORT="\$(sed -n 's/^port:[[:space:]]*//p' "\$CONFIG_PATH" | head -n 1 | tr -d '\"' | tr -d '\r' || true)"
  if [ -z "\$PORT" ]; then
    PORT="8318"
  fi
  BASE_URL="http://127.0.0.1:\$PORT"
fi

MANAGEMENT_KEY="\$(tr -d '\r\n' < "\$KEY_FILE")"

if [ -z "\$MANAGEMENT_KEY" ]; then
  echo "management key file is empty: \$KEY_FILE" >&2
  exit 1
fi

exec "$binary_path" --cpa-base-url "\$BASE_URL" -k "\$MANAGEMENT_KEY" "\$@"
EOF
  chmod 755 "$target"
}

write_service_unit() {
  local target="$1"
  local binary_path="$2"
  local config_path="$3"

  mkdir -p "$(dirname "$target")"
  cat > "$target" <<EOF
[Unit]
Description=CLIProxyAPI User Service
After=network-online.target
Wants=network-online.target

[Service]
Type=simple
ExecStart=$binary_path -config $config_path
Restart=always
RestartSec=5
WorkingDirectory=$APP_DIR

[Install]
WantedBy=default.target
EOF
}

write_systemd_install_wrapper() {
  local target="$1"
  local service_path="$2"

  cat > "$target" <<EOF
#!/usr/bin/env bash
set -euo pipefail

SERVICE_PATH="$service_path"

command -v systemctl >/dev/null 2>&1 || {
  echo "systemctl not found" >&2
  exit 1
}

systemctl --user daemon-reload
systemctl --user enable --now "$(basename "$SERVICE_PATH")"
systemctl --user status "$(basename "$SERVICE_PATH")" --no-pager || true
EOF
  chmod 755 "$target"
}

write_systemd_uninstall_wrapper() {
  local target="$1"
  local service_path="$2"

  cat > "$target" <<EOF
#!/usr/bin/env bash
set -euo pipefail

SERVICE_NAME="$(basename "$service_path")"

if command -v systemctl >/dev/null 2>&1; then
  systemctl --user disable --now "\$SERVICE_NAME" >/dev/null 2>&1 || true
  systemctl --user daemon-reload >/dev/null 2>&1 || true
fi
EOF
  chmod 755 "$target"
}

write_systemd_control_wrapper() {
  local target="$1"
  local verb="$2"
  local service_path="$3"

  cat > "$target" <<EOF
#!/usr/bin/env bash
set -euo pipefail
exec systemctl --user $verb "$(basename "$service_path")" "\$@"
EOF
  chmod 755 "$target"
}

write_systemd_logs_wrapper() {
  local target="$1"
  local service_path="$2"

  cat > "$target" <<EOF
#!/usr/bin/env bash
set -euo pipefail
exec journalctl --user -u "$(basename "$service_path")" -n 200 -f "\$@"
EOF
  chmod 755 "$target"
}

config_has_bcrypt_secret() {
  local config_path="$1"
  grep -Eq '^[[:space:]]*secret-key:[[:space:]]*"\$2[aby]\$' "$config_path"
}

bootstrap_management_secret() {
  local binary_path="$1"
  local config_path="$2"
  local bootstrap_log="$TMP_DIR/bootstrap.log"
  local pid=""

  if config_has_bcrypt_secret "$config_path"; then
    return
  fi

  log "Bootstrapping CLIProxyAPI once to hash the management secret"
  "$binary_path" -config "$config_path" >"$bootstrap_log" 2>&1 &
  pid=$!

  for _ in $(seq 1 20); do
    if config_has_bcrypt_secret "$config_path"; then
      break
    fi
    if ! kill -0 "$pid" >/dev/null 2>&1; then
      break
    fi
    sleep 1
  done

  kill "$pid" >/dev/null 2>&1 || true
  wait "$pid" 2>/dev/null || true

  if config_has_bcrypt_secret "$config_path"; then
    log "Management secret hashed successfully"
  else
    log "Bootstrap hash step did not complete; first manual start may still be needed"
    if [ -s "$bootstrap_log" ]; then
      sed -n '1,20p' "$bootstrap_log" >&2 || true
    fi
  fi
}

install_release_binary() {
  local repo="$1"
  local asset_name="$2"
  local output_path="$3"
  local extracted_name="$4"
  local asset_url

  asset_url="$(fetch_release_asset_url "$repo" "$asset_name")"
  log "Downloading $repo asset $asset_name"
  curl -fsSL "$asset_url" -o "$TMP_DIR/$asset_name"
  tar -xzf "$TMP_DIR/$asset_name" -C "$TMP_DIR"
  install -m 755 "$TMP_DIR/$extracted_name" "$output_path"
}

main() {
  require_cmd curl
  require_cmd tar
  require_cmd python3
  require_cmd openssl
  require_cmd awk

  [ "$(uname -s)" = "Linux" ] || fail "This installer supports Linux only."

  local arch
  local management_key
  local cli_version
  local cpaq_version

  arch="$(detect_arch)"
  management_key="$(random_secret)"

  mkdir -p "$APP_DIR" "$BIN_DIR" "$CONFIG_DIR"
  mkdir -p "$SYSTEMD_USER_DIR"
  chmod 700 "$CONFIG_DIR"

  cli_version="$(fetch_release_version "$CLIPROXYAPI_REPO")"
  cpaq_version="$(fetch_release_version "$CPAQ_REPO")"

  install_release_binary \
    "$CLIPROXYAPI_REPO" \
    "CLIProxyAPI_${cli_version}_linux_${arch}.tar.gz" \
    "$APP_DIR/cli-proxy-api" \
    "cli-proxy-api"

  install_release_binary \
    "$CPAQ_REPO" \
    "CLIProxyAPI-Quota-Inspector_${cpaq_version}_linux_${arch}.tar.gz" \
    "$APP_DIR/cpa-quota-inspector" \
    "cpa-quota-inspector"

  if [ ! -f "$CONFIG_DIR/management.key" ]; then
    printf '%s\n' "$management_key" > "$CONFIG_DIR/management.key"
    chmod 600 "$CONFIG_DIR/management.key"
    log "Generated management key at $CONFIG_DIR/management.key"
  else
    management_key="$(tr -d '\r\n' < "$CONFIG_DIR/management.key")"
    log "Keeping existing management key at $CONFIG_DIR/management.key"
  fi

  if [ "$FORCE_CONFIG" = "1" ] && [ -f "$CONFIG_PATH" ]; then
    cp -f "$CONFIG_PATH" "$CONFIG_PATH.bak.$(date +%Y%m%d-%H%M%S)"
    log "Backed up existing config before overwrite"
  fi

  if [ ! -f "$CONFIG_PATH" ] || [ "$FORCE_CONFIG" = "1" ]; then
    render_config "$CONFIG_PATH" "$DEFAULT_API_KEY" "$management_key" "$DEFAULT_PORT"
    chmod 600 "$CONFIG_PATH"
    cp -f "$CONFIG_PATH" "$CONFIG_DIR/config.yaml"
    chmod 600 "$CONFIG_DIR/config.yaml"
    log "Wrote template config to $CONFIG_PATH"
    bootstrap_management_secret "$APP_DIR/cli-proxy-api" "$CONFIG_PATH"
    cp -f "$CONFIG_PATH" "$CONFIG_DIR/config.yaml"
    chmod 600 "$CONFIG_DIR/config.yaml"
  else
    cp -f "$CONFIG_PATH" "$CONFIG_DIR/config.yaml"
    chmod 600 "$CONFIG_DIR/config.yaml"
    log "Keeping existing config at $CONFIG_PATH"
  fi

  write_cli_wrapper "$BIN_DIR/cliproxyapi" "$APP_DIR/cli-proxy-api"
  write_start_wrapper "$BIN_DIR/cliproxyapi-start" "$APP_DIR/cli-proxy-api" "$CONFIG_PATH"
  write_login_wrapper "$BIN_DIR/cliproxyapi-codex-login" "$APP_DIR/cli-proxy-api" "-codex-login" "$CONFIG_PATH"
  write_login_wrapper "$BIN_DIR/cliproxyapi-claude-login" "$APP_DIR/cli-proxy-api" "-claude-login" "$CONFIG_PATH"
  write_login_wrapper "$BIN_DIR/cliproxyapi-antigravity-login" "$APP_DIR/cli-proxy-api" "-antigravity-login" "$CONFIG_PATH"
  write_login_wrapper "$BIN_DIR/cliproxyapi-gemini-login" "$APP_DIR/cli-proxy-api" "-login" "$CONFIG_PATH"
  write_cpaq_wrapper "$BIN_DIR/cpaq" "$APP_DIR/cpa-quota-inspector" "$CONFIG_PATH" "$CONFIG_DIR/management.key"
  write_service_unit "$SERVICE_PATH" "$APP_DIR/cli-proxy-api" "$CONFIG_PATH"
  write_systemd_install_wrapper "$BIN_DIR/cliproxyapi-service-install" "$SERVICE_PATH"
  write_systemd_uninstall_wrapper "$BIN_DIR/cliproxyapi-service-uninstall" "$SERVICE_PATH"
  write_systemd_control_wrapper "$BIN_DIR/cliproxyapi-service-start" "start" "$SERVICE_PATH"
  write_systemd_control_wrapper "$BIN_DIR/cliproxyapi-service-stop" "stop" "$SERVICE_PATH"
  write_systemd_control_wrapper "$BIN_DIR/cliproxyapi-service-restart" "restart" "$SERVICE_PATH"
  write_systemd_control_wrapper "$BIN_DIR/cliproxyapi-service-status" "status --no-pager" "$SERVICE_PATH"
  write_systemd_logs_wrapper "$BIN_DIR/cliproxyapi-service-logs" "$SERVICE_PATH"

  ensure_path_line

  log "CLIProxyAPI version: $cli_version"
  log "CLIProxyAPI-Quota-Inspector version: $cpaq_version"
  log "Install root: $INSTALL_ROOT"
  log "Config path: $CONFIG_PATH"
  log "OAuth/auth dir: $CONFIG_DIR"
  log "Commands:"
  log "  cliproxyapi-start"
  log "  cliproxyapi-codex-login"
  log "  cliproxyapi-claude-login"
  log "  cliproxyapi-antigravity-login"
  log "  cliproxyapi-gemini-login"
  log "  cpaq"
  log "  cliproxyapi-service-install"
  log "  cliproxyapi-service-uninstall"
  log "  cliproxyapi-service-start"
  log "  cliproxyapi-service-stop"
  log "  cliproxyapi-service-restart"
  log "  cliproxyapi-service-status"
  log "  cliproxyapi-service-logs"
}

main "$@"
